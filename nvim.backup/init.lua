-- Acivate jrun (to compile and execute a java program in a floating terminal)
vim.api.nvim_set_keymap('n', '<leader>jr', ":lua require('java_runner').run_java()<CR>", { noremap = true, silent = true })

-- Enable TAB as way to switch buffers
